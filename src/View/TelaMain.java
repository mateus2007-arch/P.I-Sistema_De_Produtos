 /**
 * Método responsável por carregar os produtos na JTable.
 * 
 * Utiliza o DefaultTableModel para inserir os registros
 * na tabela exibida na tela principal do sistema.
 * 
 * Esse método é chamado quando a tela é iniciada ou quando
 * é necessário atualizar os dados exibidos.
 */
private void carregarProdutos(){

    DefaultTableModel model = (DefaultTableModel) jTableProdutos.getModel();
    model.setRowCount(0);

    ProdutoDAO dao = new ProdutoDAO();

    for(Produto p : dao.listar()){
        model.addRow(new Object[]{
            p.getId(),
            p.getNome(),
            p.getCategoria(),
            p.getMarca(),
            p.getPreco(),
            p.getEstoque()
        });
    }
}

