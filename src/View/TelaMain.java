    private void jCombHardwareActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCombHardwareActionPerformed
  String categoria = jCombHardware.getSelectedItem().toString();

    // Se for "Todos", mostra tudo
    if (categoria.equals("Todos")) {
        carregarProdutos();
        return;
    }

    ProdutoDAO dao = new ProdutoDAO();
    List<Produto> lista = dao.buscarPorCategoria(categoria);

    DefaultTableModel modelo = (DefaultTableModel) jTableProdutos.getModel();
    modelo.setRowCount(0);

    for (Produto p : lista) {
        modelo.addRow(new Object[]{
            p.getId(),
            p.getNome(),
            p.getCategoria(),
            p.getMarca(),
            p.getPreco(),
            p.getEstoque()
        });
    }  
    }//GEN-LAST:event_jCombHardwareActionPerformed
