    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
      
    int linha = jTableProdutos.getSelectedRow();

    if (linha == -1) {
        javax.swing.JOptionPane.showMessageDialog(null, "Selecione um produto!");
        return;
    }

    int id = Integer.parseInt(jTableProdutos.getValueAt(linha, 0).toString());
    String nome = jTableProdutos.getValueAt(linha, 1).toString();
    String categoria = jTableProdutos.getValueAt(linha, 2).toString();
    String marca = jTableProdutos.getValueAt(linha, 3).toString();
    String preco = jTableProdutos.getValueAt(linha, 4).toString();
    String estoque = jTableProdutos.getValueAt(linha, 5).toString();

    EditarP tela = new EditarP();
    tela.setDados(id, nome, categoria, marca, preco, estoque);
    tela.setVisible(true);
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int linha = jTableProdutos.getSelectedRow();

    if (linha == -1) {
        javax.swing.JOptionPane.showMessageDialog(null, "Selecione um produto!");
        return;
    }
    int confirm = javax.swing.JOptionPane.showConfirmDialog(
        null,
        "Tem certeza que deseja excluir?",
        "Confirmação",
        javax.swing.JOptionPane.YES_NO_OPTION
    );
    if (confirm == javax.swing.JOptionPane.YES_OPTION) {

        int id = Integer.parseInt(
            jTableProdutos.getValueAt(linha, 0).toString()
        );

        ProdutoDAO dao = new ProdutoDAO();
        dao.deletar(id);
        javax.swing.JOptionPane.showMessageDialog(null, "Produto excluído!");
        carregarProdutos(); 
    }
    
    }//GEN-LAST:event_btnExcluirActionPerformed

